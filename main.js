
function checkAnswer1() {
    var inputElement = document.getElementById("question1");
    var inputValue = inputElement.value;
    if(inputValue=="")
    {
        var para = document.getElementById("correct1");
         para.style.display = "none";
         var no = document.getElementById("incorrect1");
         no.style.display = "none";
    }
    else if(inputValue==1911)
    {
        var para = document.getElementById("correct1");
         para.style.display = "block";
         var no = document.getElementById("incorrect1");
         no.style.display = "none";
    }
    else{
        var para = document.getElementById("correct1");
         para.style.display = "none";
         var no = document.getElementById("incorrect1");
         no.style.display = "block";
    }
  }
  function resetAnswer1(){
    var para = document.getElementById("correct1");
         para.style.display = "none";
         var no = document.getElementById("incorrect1");
         no.style.display = "none";
  }
  function nextQuestion1(){
    var close = document.querySelector('.answer')
       close.style.display="none"
    var close = document.querySelector('.question1')
       close.style.display="none"
       var close = document.querySelector('.question2')
       close.style.display="block"
  }



